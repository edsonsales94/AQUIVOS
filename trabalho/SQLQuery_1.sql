SELECT 	F2_DOC,     
        A3_NOME,     
        A3_COD,     
        A3_COMIS,     
        B1_COD,     
        B1_DESC,     
        D2_TOTAL,    
        D2_COMIS1,     
        SUM(COMISSAO) COMISSAO  
FROM    (SELECT	F2.F2_DOC, 
        A3.A3_COD, 
        A3.A3_NOME, 
        A3.A3_COMIS, 
        B1.B1_COD, 
        B1.B1_DESC, 
        D2.D2_TOTAL,
        D2.D2_COMIS1,
        D2.D2_TOTAL*(D2.D2_COMIS1/100) COMISSAO
FROM SF2010 F2 (NOLOCK)         
INNER JOIN 	SA3010 A3 (NOLOCK) ON  A3.D_E_L_E_T_='' AND A3.A3_FILIAL='' AND A3.A3_COD=F2.F2_VEND1 AND A3.A3_XSEGUI='GV' AND A3.A3_COD ='000382'        
INNER JOIN 	SD2010 D2 (NOLOCK) ON  D2.D_E_L_E_T_='' AND D2.D2_FILIAL=F2.F2_FILIAL  AND D2.D2_FILIAL=F2.F2_FILIAL AND D2.D2_DOC=F2.F2_DOC AND D2.D2_SERIE=F2.F2_SERIE         AND D2.D2_DOC=F2.F2_DOC AND D2.D2_SERIE=F2.F2_SERIE         
INNER JOIN 	SB1010 B1 (NOLOCK) ON  B1.D_E_L_E_T_='' AND B1.B1_FILIAL='' AND B1.B1_COD=D2.D2_COD         
WHERE   F2.D_E_L_E_T_=''                 AND F2.F2_EMISSAO BETWEEN '20220301' AND '20220331'                                  AND F2.F2_TIPO='N'         
UNION ALL     
SELECT A.D1_DOC, F.A3_COD, F.A3_NOME, F.A3_COMIS, E.B1_COD, E.B1_DESC, A.D1_TOTAL*(-1), D.D2_COMIS1, (A.D1_TOTAL*D.D2_COMIS1/100) * (-1)         
FROM SD1010 A (NOLOCK)         INNER JOIN SF4010 B (NOLOCK) ON B.D_E_L_E_T_='' AND B.F4_FILIAL='' AND B.F4_CODIGO=A.D1_TES AND B.F4_DUPLIC='S'         
INNER JOIN SF2010 C (NOLOCK) ON C.D_E_L_E_T_='' AND C.F2_FILIAL=A.D1_FILIAL AND C.F2_DOC=A.D1_NFORI AND C.F2_SERIE=A.D1_SERIORI         
INNER JOIN SD2010 D (NOLOCK) ON D.D_E_L_E_T_='' AND D.D2_FILIAL=C.F2_FILIAL AND D.D2_DOC=C.F2_DOC AND D.D2_SERIE=C.F2_SERIE AND D.D2_ITEM=A.D1_ITEMORI         
INNER JOIN SB1010 E (NOLOCK) ON E.D_E_L_E_T_='' AND E.B1_FILIAL='' AND E.B1_COD=D.D2_COD         
INNER JOIN SA3010 F (NOLOCK) ON F.D_E_L_E_T_='' AND F.A3_FILIAL='' AND F.A3_COD=C.F2_VEND1 AND F.A3_XSEGUI='VA'         
LEFT  JOIN SA3010 X (NOLOCK) ON X.D_E_L_E_T_='' AND X.A3_FILIAL='' AND X.A3_COD=C.F2_VEND2         WHERE	A.D_E_L_E_T_=''   AND A.D1_TIPO='D'                                  AND A.D1_EMISSAO BETWEEN '20220301' AND '20220331'         ) AS T1        
GROUP BY T1.F2_DOC, T1.A3_COD,T1.A3_NOME,T1.A3_COMIS, T1.B1_COD, T1.B1_DESC, T1.D2_TOTAL,T1.D2_COMIS1, T1.COMISSAO         
ORDER BY T1.A3_COD




